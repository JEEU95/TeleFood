    public void llenar() throws Exception {
        //SELECT  From Pedido ;
        //Array String, String, String

        ArrayList<String> campo = new ArrayList();
        campo.add("Nombre");
        campo.add("EstaEnOferta");
        campo.add("Precio");
        campo.add("ProductoId");
        Registro r = new Registro();
        r.setDatos(campo);
        r.setDatos("Producto");
        r.setDatos("Tipo=0");
        ArrayList<Registro> registro = pedidos.listarDatos(r);
        
        DefaultTableModel tm = (DefaultTableModel) tbPedidos.getModel();
        tm.setRowCount(0);
        int i = 0;
        for (Registro reg : registro) {
            int j = 2;
            tm.addRow(new Object[]{""});
            tm.setValueAt(false, i, 0);
            tm.setValueAt(0, i, 1);
            for (Object dat : reg.getDatos()) {
                switch (j) {
                    case 2:
                        tm.setValueAt(dat.toString(), i, j);
                        break;
                    case 3:
                        tm.setValueAt(Boolean.valueOf(dat.toString()), i, j);
                        break;
                    case 4:
                        tm.setValueAt(Double.parseDouble(dat.toString()), i, j);
                        break;
                    case 5:
                        listaIds.add(Integer.valueOf(dat.toString()));
                        break;
                    default:
                        break;
                }
                j++;
            }
            i++;
        }

    }


    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed

        DefaultTableModel tm = (DefaultTableModel) tbPedidos.getModel();
        for (int i = 0; i < tm.getRowCount(); i++) {
            if (Boolean.valueOf(tbPedidos.getValueAt(i, 0).toString()) == true) {
                Producto prod = new Producto();
                prod.setCantidad(tbPedidos.getValueAt(i, 1).toString());
                prod.setNombre(tbPedidos.getValueAt(i, 2).toString());
                prod.setPrecio(Double.parseDouble(tbPedidos.getValueAt(i, 4).toString()));
                prod.setProductoId(listaIds.get(i));
                listaProductos.add(prod);
            }
        }
        
        int numeroTarjeta = Integer.parseInt(jSpinner1.getValue().toString());
        
        //Insertar Pedido
        //INSERT INTO Pedido VALUES(default, T+numeroTarjeta, curdate(), curtime(), 0)
        Registro reg = new Registro();
        reg.setDatos("DEFAULT");
        reg.setDatos("T"+numeroTarjeta);
        reg.setDatos("curdate()");
        reg.setDatos("curtime()");
        reg.setDatos(1);
        
        try {
            pedidos.insertarDatos("Pedido", reg);
        } catch (Exception ex) {
            System.err.println("Error: " + ex.getMessage());
        }
        
        
        for (int i = 0; i < listaProductos.size(); i++) {
            Producto prodActual = listaProductos.get(i);
            String prodId = String.valueOf(prodActual.getProductoId());
            String prodCantidad = prodActual.getCantidad();
            String prodPrecio = String.valueOf(prodActual.getPrecio());
            
            reg = new Registro();
            reg.setDatos("(SELECT MAX(PedidoId) FROM Pedido)");
            reg.setDatos(prodId);
            reg.setDatos(null);
            reg.setDatos(prodCantidad);
            reg.setDatos(prodPrecio);

            //INSERT INTO Pedido_Producto VALUES((SELECT MAX(PedidoId) FROM Pedido),
            // prodId, null, prodCantidad, prodPrecio)
            try {
                pedidos.insertarDatos("Pedido_Producto", reg);
            } catch (Exception ex) {
                System.err.println("Error: " + ex.getMessage());
            }
        }
        
        dispose();
    }//GEN-LAST:event_btnSiguienteActionPerformed

